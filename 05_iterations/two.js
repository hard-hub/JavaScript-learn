// while
let index = 1;
while (index <= 10) {
    console.log(index);
    index += 2;
}

let myArray = ['flash', "batman", "spiderman"]
let x = 0;
while(x < myArray.length) {
    console.log(myArray[x]);
    x++;
}


// do while
let score = 1;
do {
    console.log(`Score is : ${score}`);
    score++;
} while (score <= 10);